def game():
    try:
        # Запитуємо кількість монет у скарбниці
        coins = int(input("Введіть кількість золотих монет (від 1 до 1000): "))
        if coins < 1 or coins > 1000:
            raise ValueError("Кількість монет повинна бути від 1 до 1000!")

        # Запитуємо кількість людей
        people = int(input("Введіть кількість людей в команді: "))
        
        # Перевіряємо, щоб кількість людей була більше 0
        if people <= 0:
            raise ValueError("Кількість людей повинна бути більшою за 0!")

        # Обчислюємо кількість монет на кожного гравця
        coins_per_person = coins // people
        print(f"Кожному гравцеві дісталося {coins_per_person} монет.")

    except ValueError as e:
        # Обробляємо випадок, коли введено некоректне число або кількість монет/людей не підходить
        print(f"Помилка: {e}")
    
    except ZeroDivisionError:
        # Обробляємо випадок, коли кількість людей = 0
        print("Помилка: неможливо поділити на нуль (кількість людей не може бути нульовою).")
    
    finally:
        # Цей блок виконується завжди
        print("Пригоди тривають!")

# Запуск гри
game()
